package com.augmentum.ot.dataObject;

public class ScoreForShow {
	
	private String itemName;
	
	private double  assessScore;
	
	private String assessItemDescribe;
	
	private Integer itemId;
	
	private Integer scoreId;

	public String getItemName() {
		return itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

	public double getAssessScore() {
		return assessScore;
	}

	public void setAssessScore(double assessScore) {
		this.assessScore = assessScore;
	}

	public String getAssessItemDescribe() {
		return assessItemDescribe;
	}

	public void setAssessItemDescribe(String assessItemDescribe) {
		this.assessItemDescribe = assessItemDescribe;
	}

	public Integer getItemId() {
		return itemId;
	}

	public void setItemId(Integer itemId) {
		this.itemId = itemId;
	}

	public Integer getScoreId() {
		return scoreId;
	}

	public void setScoreId(Integer scoreId) {
		this.scoreId = scoreId;
	}
	
}
